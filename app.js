function ShowHideMenu () {
    var lista = document.getElementsByClassName("list")[0] || document.getElementsByClassName("list-Hide")[0]; 
    if (lista.className === "list-Hide") {
        lista.className = "list";
    } else {
        lista.className = "list-Hide";
    }
}
